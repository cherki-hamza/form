                        <h6 style="text-decoration: underline;">تعليمات العناية بأعمال النجارة (MDF المقاوم للرطوبة)</h6>
                        <p>بالنسبة لأعمال النجارة المصنوعة من <strong>اللوح الليفي متوسط الكثافة المقاوم للرطوبة (MR MDF)</strong>، من الضروري الحفاظ على درجة حرارة ورطوبة ثابتة لتجنب التشوه، أو التورم، أو الانكماش. فيما يلي الظروف المثلى:</p>

                        <h6 style="text-decoration: underline;">درجة الحرارة:</h6>
                        <ul>
                            <li><strong>المدى المثالي:</strong> 18°C إلى 24°C (64°F إلى 75°F).</li>
                            <li><strong>تجنب الظروف القصوى:</strong> لا تعرض MDF المقاوم للرطوبة لدرجات حرارة أقل من 10°C (50°F) أو أعلى من 30°C (86°F) لفترات طويلة، حيث يمكن أن يؤثر ذلك على استقرار المادة.</li>
                        </ul>

                        <h6 style="text-decoration: underline;">الرطوبة:</h6>
                        <ul>
                            <li><strong>المدى المثالي:</strong> 40% إلى 60% نسبة الرطوبة.</li>
                            <li><strong>تجنب:</strong> مستويات الرطوبة فوق 70% قد تسبب امتصاص المادة للرطوبة، مما يؤدي إلى التورم، بينما يمكن أن تؤدي مستويات الرطوبة تحت 30% إلى جفاف المادة، مما يسبب تشققات أو انكماش.</li>
                        </ul>

                        <h6 style="text-decoration: underline;">إرشادات التنظيف:</h6>
                        <ul>
                            <li><strong>استخدام قطعة قماش مبللة:</strong> نظف السطح باستخدام قطعة قماش مبللة قليلًا بدلاً من نقعه بالماء. تأكد من عصر القماش جيدًا لتقليل تعرض السطح للرطوبة.</li>
                            <li><strong>التجفيف الفوري:</strong> بعد المسح، تأكد من تجفيف السطح بسرعة بقطعة قماش جافة لمنع تراكم الرطوبة على السطح.</li>
                            <li><strong>تجنب المنظفات القاسية:</strong> لا تستخدم الماء الزائد أو منتجات التنظيف التي قد تضيف رطوبة زائدة إلى المادة.</li>
                        </ul>

                        <p><strong>ملاحظة:</strong> <em>عدم الحفاظ على هذه الظروف أو عدم العناية بشكل صحيح سيؤدي إلى إبطال الضمان</em> على منتجات النجارة المصنوعة من MDF المقاوم للرطوبة.</p>
