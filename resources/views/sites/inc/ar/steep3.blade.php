                        <h6 style="text-decoration: underline;">تعليمات العناية الخاصة بأسطح البورسلين</h6>
                        <p>تتطلب أسطح البورسلين عناية خاصة نظرًا لتركيبها. على عكس المواد الكاملة مثل الرخام أو الجرانيت، فإن أسطح البورسلين أكثر عرضة للتلف بسبب الصدمات أو منتجات التنظيف أو الاستخدام غير الصحيح. يُرجى اتباع الإرشادات أدناه للحفاظ على مظهر ومتانة أسطح البورسلين:</p>

                        <h6 style="text-decoration: underline;">التعامل والاستخدام:</h6>
                        <ul>
                            <li><strong>تحذير من الصدمات:</strong> أسطح البورسلين ليست مواد كاملة مثل الرخام أو الجرانيت. قد يؤدي ضرب أو طرق الحواف أو السطح إلى <strong>تشقق</strong> يظهر المادة الأساسية.
                                <br><em>نصيحة:</em> تجنب وضع أشياء ثقيلة أو صلبة بالقرب من الحواف، وكن حذرًا عند التعامل مع العناصر على السطح.</li>
                            <li><strong>استخدام غطاء الحوض:</strong>
                                <ul>
                                    <li>يُوصى بعدم استخدام غطاء الحوض بشكل متكرر في المناطق ذات الحركة العالية مثل المطابخ أو الأماكن التي يتم تنظيفها كثيرًا.</li>
                                    <li><strong>العناية السليمة:</strong> عند إزالة أو وضع غطاء الحوض، تعامل معه بحذر لتجنب طرق أو خدش البورسلين. أي ضرر ناتج عن التعامل غير الصحيح <strong>لن يتم تغطيته تحت الضمان</strong>.</li>
                                </ul>
                            </li>
                        </ul>

                        <h6 style="text-decoration: underline;">إرشادات التنظيف:</h6>
                        <ul>
                            <li><strong>تجنب المواد الكيميائية القوية:</strong> استخدام مواد تنظيف قوية أو كاشطة يمكن أن يتسبب في <strong>تغير لون الجص والإبوكسي</strong> المستخدم حول أسطح البورسلين.
                                <ul>
                                    <li><strong>منظفات موصى بها:</strong> استخدم مواد تنظيف خفيفة مثل صابون الأطباق المخفف أو منظف متعادل الحموضة لتنظيف السطح والجص دون الإضرار باللمعان أو التسبب في تغير اللون.</li>
                                    <li><strong>تجنب المنظفات الحمضية:</strong> تجنب المنتجات التي تحتوي على مبيض، أو أمونيا، أو مركبات حمضية لأنها قد تضر بالبورسلين والجص.</li>
                                </ul>
                            </li>
                            <li><strong>أدوات تنظيف ناعمة:</strong> استخدم دائمًا مناشف أو إسفنجات ناعمة وغير كاشطة لتجنب خدش السطح. بالنسبة لخطوط الجص، استخدم فرشاة ذات شعيرات ناعمة لتنظيف دون الإضرار بالمواد.</li>
                        </ul>

                        <h6 style="text-decoration: underline;">إخلاء المسؤولية عن الضمان:</h6>
                        <ul>
                            <li><strong>تشققات وأضرار الصدمات:</strong> أي ضرر ناتج عن ضرب أو إسقاط أشياء على السطح أو الحواف البورسلينية <strong>لن يتم تغطيته تحت الضمان</strong>.</li>
                            <li><strong>تغير اللون بسبب المواد الكيميائية القوية:</strong> الضرر الذي يلحق بالجص أو الإبوكسي بسبب استخدام مواد تنظيف قوية أو غير مناسبة <strong>لن يتم تغطيته تحت الضمان</strong>.</li>
                        </ul>

                        <p>من خلال اتباع تعليمات العناية هذه، يمكنك الحفاظ على جمال ووظيفة أسطح البورسلين لسنوات طويلة.</p>
