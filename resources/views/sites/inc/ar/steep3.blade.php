                        <h6 style="text-decoration: underline;">تعليمات العناية الخاصة بالمرايا وإطارات LED</h6>
                        <p>تتطلب المرايا المزودة بإضاءة LED وإطاراتها ممارسات تنظيف وعناية محددة لتجنب التلف. المرايا، خاصة تلك ذات الإطارات الملونة، يمكن أن تتأثر ببعض مواد التنظيف، مما يؤدي إلى تغير لونها أو تدهور حالتها. يُرجى اتباع الإرشادات التالية لضمان طول عمر المرايا وإطارات LED:</p>

                        <h6 style="text-decoration: underline;">إرشادات تنظيف المرايا:</h6>
                        <ul>
                            <li><strong>تجنب المواد الكيميائية القاسية:</strong> يمكن أن تتعرض المرايا للتلف بواسطة منتجات التنظيف القوية أو الكاشطة، خاصة تلك التي تحتوي على الأمونيا أو المبيض أو المركبات الحمضية.
                                <ul>
                                    <li><strong>طريقة التنظيف الموصى بها:</strong> استخدم <em>قطعة قماش مبللة</em> لتنظيف سطح المرآة بلطف. هذا يكفي لإزالة بصمات الأصابع والغبار والبقع الطفيفة دون المخاطرة بتلف الطلاء العاكس.</li>
                                    <li><strong>الحذر مع الحواف:</strong> كن حذرًا عند تنظيف حواف المرآة، حيث يمكن أن يؤدي التعرض الزائد للرطوبة أو المواد الكيميائية إلى ضعف خلفية المرآة وتسبب في تقشيرها أو تغير لونها مع مرور الوقت.</li>
                                </ul>
                            </li>
                        </ul>

                        <h6 style="text-decoration: underline;">العناية الخاصة بالإطارات الملونة:</h6>
                        <ul>
                            <li><strong>تجنب جميع مواد التنظيف على الإطارات:</strong> الإطارات الملونة حول المرايا، خاصة تلك ذات الطلاء، يمكن أن تتلف بسهولة بسبب المواد الكيميائية التنظيفية.
                                <ul>
                                    <li><strong>طريقة التنظيف الموصى بها:</strong> يجب مسح الإطار فقط باستخدام <em>قطعة قماش ناعمة جافة أو مبللة قليلاً</em>. تجنب استخدام أي مواد تنظيف على الإطار للحفاظ على لونه وسلامته.</li>
                                    <li><strong>عدم التعرض للماء:</strong> كن حذرًا بشأن تراكم الماء أو الرطوبة حول الإطار، حيث يمكن أن يؤثر التعرض المطول على اللون والمواد.</li>
                                </ul>
                            </li>
                        </ul>

                        <h6 style="text-decoration: underline;">إخلاء المسؤولية عن الضمان:</h6>
                        <ul>
                            <li><strong>تلف المرايا بسبب مواد التنظيف:</strong> أي تلف على سطح المرآة أو الإطار بسبب استخدام مواد تنظيف غير مناسبة أو قاسية <strong>لن يتم تغطيته تحت الضمان</strong>.</li>
                            <li><strong>تغير اللون أو تقشر الإطار:</strong> أي تلف أو تغير في لون الإطار بسبب استخدام مواد التنظيف الكيميائية أو طرق التنظيف غير السليمة <strong>لن يتم تغطيته تحت الضمان</strong>.</li>
                        </ul>

                        <p>باتباع تعليمات العناية هذه، يمكنك الحفاظ على جودة ومظهر المرايا المزودة بإضاءة LED وإطاراتها لسنوات طويلة.</p>
